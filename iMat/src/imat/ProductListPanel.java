    public void updateProducts(List<Product> products) {
        reset();
        
        if(products.size() > 8){
            grid.setRows((int)(products.size()/4));
        }else{
            grid.setRows(2);
        }
        for (Product product : products) {
            ProductPanel panel = new ProductPanel(product);
            gridPanel.add(panel);
        }
        
        gridPanel.revalidate();
        gridPanel.repaint();
    }

